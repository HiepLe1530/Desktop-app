    private void btnThemHDDVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemHDDVActionPerformed
        // TODO add your handling code here:
        HoaDonDV hddv = new HoaDonDV();
        hddv.setMaHDDV(txtMaHDDV.getText());
        try {
            hddv.setNgayLap(new SimpleDateFormat("dd/MM/yyyy").parse(txtNgayLap.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(GDHoaDonDV.class.getName()).log(Level.SEVERE, null, ex);
        }
        hddv.setTongTien(Long.parseLong(txtTongTien.getText()));
        if(new HoaDonDVDAO().addHoaDonDV(hddv)){
            themHDDV(hddv);
            new GDTrangChu().updatePDV(hddv.getMaHDDV());
        }
    }//GEN-LAST:event_btnThemHDDVActionPerformed

