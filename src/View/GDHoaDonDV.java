    void editThongTin(String p) {
        dsDichVu = new DichVuDAO().getListDichVu(p);
        dsPhieuDichVu = new PhieuDichVuDAO().getListPhieuDichVu(p);
        txtMaHDDV.setText(dsPhieuDichVu.get(0).getMaHDDV());
        long tongTien = tongTienDV(dsDichVu, dsPhieuDichVu);
        txtTongTien.setText(tongTien+"");
        
        showDV();
        showPDV();
    }
    
    void showDV(){
        for(DichVu dv : dsDichVu){
            tblDichVuMD.addRow(new Object[]{dv.getMaDV(), dv.getTenDV(), dv.getDonGia()});
        }
    }
    
    void showPDV(){
        for(PhieuDichVu Pdv : dsPhieuDichVu){
            tblPhieuDichVuMD.addRow(new Object[]{Pdv.getMaHDDV(), Pdv.getMaDV(), Pdv.getMaP(), 
                Pdv.getSoLuong(), Pdv.getNgaySuDung()});
            } 
    }
    
    long tongTienDV(ArrayList<DichVu> dsDichVu, ArrayList<PhieuDichVu> dsPhieuDichVu ){
        int n = dsDichVu.size();
        long tongTien = 0;
        for(int i=0; i<n; i++){
            tongTien += dsPhieuDichVu.get(i).getSoLuong() * dsDichVu.get(i).getDonGia();
        }
        return tongTien;
    }

    private void themHDDV(HoaDonDV hddv) {
        dsHoaDonDV.add(hddv);
        tblHoaDonDVMD.setRowCount(0);
        showHDDV();
    }

    private void showHDDV() {
        for(HoaDonDV hddv : dsHoaDonDV){
            tblHoaDonDVMD.addRow(new Object[]{hddv.getMaHDDV(), hddv.getNgayLap(), hddv.getTongTien()});
        }
    }
    
