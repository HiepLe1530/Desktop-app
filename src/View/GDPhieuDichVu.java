    private void btnaddDVtoPDVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddDVtoPDVActionPerformed
        // TODO add your handling code here:
        selectedrowDV = tblDichVu.getSelectedRow();
        if(selectedrowDV == -1){
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn dịch vụ rồi thao tác");
        }
        else if(dsDichVu.size()==0) {
            JOptionPane.showMessageDialog(rootPane, "yêu cầu thêm dịch vụ");
        }
        else{
            DichVu dv = dsDichVu.get(selectedrowDV);
            txtMaDV.setText(dv.getMaDV());
        }
    }//GEN-LAST:event_btnaddDVtoPDVActionPerformed

    private void btnThemPDVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemPDVActionPerformed
        // TODO add your handling code here:
        PhieuDichVu pdv = new PhieuDichVu();
        pdv.setMaHDDV(txtMaHDDV.getText());
        pdv.setMaP(Integer.parseInt(txtMaP.getText()));
        pdv.setMaDV(txtMaDV.getText());
        pdv.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
        try {
            pdv.setNgaySuDung(new SimpleDateFormat("dd/MM/yyyy").parse(txtNgaySuDung.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(GDPhieuDichVu.class.getName()).log(Level.SEVERE, null, ex);
        }
        String tt = "chưa đóng";
        if(rbtChuaDong.isSelected()) tt = "chưa đóng";
        if(rbtDaDong.isSelected()) tt = "đã đóng";
        pdv.setTrangThai(tt);
        if(new PhieuDichVuDAO().addPhieuDichVu(pdv)){
            themPDV(pdv);
            JOptionPane.showMessageDialog(rootPane, "Thêm phiếu dịch vụ thành công");
            new GDTrangChu().ThemPDV(pdv);
        }else JOptionPane.showMessageDialog(rootPane, "Dịch vụ này đã có trên phiếu dịch vụ -> yêu cầu cập nhật hoặc chọn dịch vụ khác");
        
            
        
        
    }//GEN-LAST:event_btnThemPDVActionPerformed

    private void btnCapNhatPDVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatPDVActionPerformed
        // TODO add your handling code here:
        selectedrowPDV = tblPhieuDichVu.getSelectedRow();
        if(selectedrowPDV == -1){
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn phiếu dịch vụ rồi thao tác");
        }
        else if(dsPhieuDichVuTK.size()==0) {
            JOptionPane.showMessageDialog(rootPane, "yêu cầu thêm phiếu dịch vụ");
        }
        else{
            GDCapNhatPDV GDCNPDV = new GDCapNhatPDV(this, rootPaneCheckingEnabled);
            GDCNPDV.seteditPDV(dsPhieuDichVuTK.get(selectedrowPDV));
            GDCNPDV.setVisible(true);
        }
    }//GEN-LAST:event_btnCapNhatPDVActionPerformed

