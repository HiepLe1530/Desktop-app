    void editPDV(Phong p) {
        dsPhieuDichVuTK = new PhieuDichVuDAO().getListPDVTK(p);
        if(dsPhieuDichVuTK.size() > 0){
            showPDVTK();
            txtMaP.setText(p.getMaP()+"");
            txtMaHDDV.setText(dsPhieuDichVuTK.get(0).getMaHDDV());
            txtMaP.setEditable(false);
            txtMaHDDV.setEditable(false);
        }
    }
    
    void themPDV(PhieuDichVu pdv){
        dsPhieuDichVuTK.add(pdv);
        tblPhieuDichVuMD.setRowCount(0);
        for(PhieuDichVu Pdv : dsPhieuDichVuTK){
        tblPhieuDichVuMD.addRow(new Object[]{Pdv.getMaHDDV(), Pdv.getMaDV(), Pdv.getMaP(), 
            Pdv.getSoLuong(), Pdv.getNgaySuDung(), Pdv.getTrangThai()});
        } 
    }
    
    void showPDVTK(){
        for(PhieuDichVu pdv : dsPhieuDichVuTK){
            tblPhieuDichVuMD.addRow(new Object[]{pdv.getMaHDDV(), pdv.getMaDV(), pdv.getMaP(), 
                pdv.getSoLuong(), pdv.getNgaySuDung(), pdv.getTrangThai()});
        }
    }

    void showDV(){
        for(DichVu dv : dsDichVu){
            tblDichVuMD.addRow(new Object[]{dv.getMaDV(), dv.getTenDV(), dv.getDonGia()});
        }
    }

    public void updatePDV(PhieuDichVu pdv) {
//        PhieuDichVu beforPDV = dsPhieuDichVuTK.get(selectedrowPDV);
        dsPhieuDichVuTK.remove(selectedrowPDV);
        themPDV(pdv);
    }


    
